package org.jbox2d.dynamics;

@SuppressWarnings("serial")
public final class SameLocationException extends Exception {

	public SameLocationException() {
		super();
	}

	public SameLocationException(String arg0) {
		super(arg0);
	}

	public SameLocationException(Throwable arg0) {
		super(arg0);
	}

	public SameLocationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
